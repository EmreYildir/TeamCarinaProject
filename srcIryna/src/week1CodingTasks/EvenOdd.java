package week1CodingTasks;

public class EvenOdd {
    public static void main(String[] args) {

        if(5 % 2 == 0){
            System.out.println(5 + "=Even");
        }else if(5 % 2 != 0){
            System.out.println(5 + "=Odd");
        }
        if(6 % 2 == 0){
            System.out.println(6 + "=Even");
        }else if(6 % 2 != 0){
            System.out.println(6 + "=Odd");
        }
    }
}
